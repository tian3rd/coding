# @lc code=start
"""
# Definition for a Node.
class Node:
    def __init__(self, val=None, children=None):
        self.val = val
        self.children = children
"""

class Solution:
    def preorder(self, root: 'Node') -> List[int]:
        if root is None:
            return []
        rtn = []
        rtn.append(root.val)
        for node in root.children:
            rtn += self.preorder(node)
        return rtn
        
# @lc code=end

